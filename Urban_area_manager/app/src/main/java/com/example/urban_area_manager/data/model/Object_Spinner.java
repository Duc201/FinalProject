package com.example.urban_area_manager.data.model;

public class Object_Spinner {

    private String name;
    private String id;

    public Object_Spinner(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
